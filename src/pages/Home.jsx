import React, { useState } from "react";
import { useNavigate } from "react-router-dom";
import useAuthStore from "../stores/useAuthStore";
import { useHomeData } from "../hooks/useHomeData";

// Components
import HomeSkeleton from "./HomeSkeleton";
import LevelModal from "../game/LevelModal";
import { ChallengeMap } from "../home";
import CertificateModal from "../components/CertificateModal";

const Home = () => {
  const navigate = useNavigate();
  const { user } = useAuthStore();

  // Use centralized data hook
  const {
    levels,
    isLoading,
    certificateModalOpen,
    setCertificateModalOpen,
    userCertificate,
    isCertificateLoading,
    handleCertificateClick,
    CERTIFICATE_SLUG
  } = useHomeData(user);

  // Local UI State
  const [selectedLevel, setSelectedLevel] = useState(null);

  const handleLevelClick = async (level) => {
    if (!user) {
      navigate("/login");
      return;
    }

    if (level.unlocked) {
      const isCertificate =
        level.slug === CERTIFICATE_SLUG || level.type === "CERTIFICATE";
      if (isCertificate) {
        handleCertificateClick();
      } else {
        setSelectedLevel(level);
      }
    }
  };

  // Removed internal user check as routing is now handled in App.jsx
  if (isLoading) {
    return (
      <div className="relative select-none text-white min-h-screen">
        <HomeSkeleton />
      </div>
    );
  }

  return (
    <div className="relative select-none text-white min-h-screen">
      <div className="w-full relative">
        <ChallengeMap
          user={user}
          levels={levels}
          handleLevelClick={handleLevelClick}
        />

        {selectedLevel && (
          <LevelModal
            selectedLevel={selectedLevel}
            onClose={() => setSelectedLevel(null)}
          />
        )}

        <CertificateModal
          isOpen={certificateModalOpen}
          onClose={() => setCertificateModalOpen(false)}
          certificate={userCertificate}
          isLoading={isCertificateLoading}
        />
      </div>
    </div>
  );
};

export default Home;
